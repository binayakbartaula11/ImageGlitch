# ImageGlitch Utilities Package
#
# This package provides specialized image manipulation utilities for creating
# various glitch and distortion effects. It includes modules for:
#
# - noise: Adding Gaussian and Salt & Pepper noise to images
# - blur: Applying Gaussian, Motion, and Box blur effects
# - shaky: Simulating camera shake and directional movement
# - motion: Creating motion distortion and zoom effects
#
# All functions accept numpy arrays as input and return processed images
# as float32 arrays to allow for further processing in the pipeline.
#
# These utilities are designed to work with the ImageGlitch application
# but can also be used independently for creative image manipulation.